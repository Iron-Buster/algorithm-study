# 返回<=t的最后一个下标
def leftBound(a: List[int], t: int) -> int:
    l, r = 0, len(a) - 1
    while l < r:
        mid = (l + r + 1) >> 1
        if a[mid] > t:
            r = mid - 1
        else:
            l = mid
    return -1 if a[l] > t else l
                                
# 返回>=t的第一个下标
def rightBound(a: List[int], t: int) -> int:
    l, r = 0, len(a) - 1
    while l < r:
        mid = (l + r) >> 1
        if a[mid] < t:
            l = mid + 1
        else:
            r = mid 
    return -1 if a[l] < t else l
    
class Solution:
    '''
        记忆化搜索
    '''
    def numRollsToTarget(self, n: int, k: int, target: int) -> int:
        @cache
        def dfs(n: int, t: int) -> int:
            if t < 0: return 0
            if n == 0: return t == 0
            ans = 0
            for i in range(1, k + 1):
                ans += dfs(n - 1, t - i)
            return ans % MOD
        return dfs(n, target) % MOD
